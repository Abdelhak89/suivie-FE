export function getClientNameFromCode(code) {
  const c = String(code || "").trim();
  return CLIENTS_BY_CODE[c] || "";
}

export function getClientCodeFromNumeroFe(numeroFe) {
  const s = String(numeroFe || "").trim();
  return s.length >= 3 ? s.slice(0, 3) : "";
}

export function getClientNameFromNumeroFe(numeroFe) {
  const code = getClientCodeFromNumeroFe(numeroFe);
  return getClientNameFromCode(code);
}
